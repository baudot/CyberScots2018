/**
 * This 2018-2019 OpMode illustrates the basics of using the Vuforia localizer to determine
 * positioning and orientation of robot on the FTC field.
 * The code is structured as a LinearOpMode
 *
 * Vuforia uses the phone's camera to inspect it's surroundings, and attempt to locate target images.
 *
 * When images are located, Vuforia is able to determine the position and orientation of the
 * image relative to the camera.  This sample code than combines that information with a
 * knowledge of where the target images are on the field, to determine the location of the camera.
 *
 * This example assumes a "square" field configuration where the red and blue alliance stations
 * are on opposite walls of each other.
 *
 * From the Audience perspective, the Red Alliance station is on the right and the
 * Blue Alliance Station is on the left.

 * The four vision targets are located in the center of each of the perimeter walls with
 * the images facing inwards towards the robots:
 *     - BlueRover is the Mars Rover image target on the wall closest to the blue alliance
 *     - RedFootprint is the Lunar Footprint target on the wall closest to the red alliance
 *     - FrontCraters is the Lunar Craters image target on the wall closest to the audience
 *     - BackSpace is the Deep Space image target on the wall farthest from the audience
 *
 * A final calculation then uses the location of the camera on the robot to determine the
 * robot's location and orientation on the field.
 *
 * @see VuforiaLocalizer
 * @see VuforiaTrackableDefaultListener
 * see  ftc_app/doc/tutorial/FTC_FieldCoordinateSystemDefinition.pdf
 *
 * Use Android Studio to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this opmode to the Driver Station OpMode list.
 *
 * IMPORTANT: In order to use this OpMode, you need to obtain your own Vuforia license key as
 *uis explained below.
 */

@Autonomous(name = "[test] Vuforia Drive", group = "Concept")
//@Disabled
public class VuforiaDriveTest extends LinearOpMode {

    VuforiaPos vpos = null;
    HardwareRagbotNoArm robot = null;



    @Override public void runOpMode() {
        robot = new HardwareRagbotNoArm();   // Use a Pushbot's hardware
        robot.init(hardwareMap);

        vpos = new VuforiaPos(robot, telemetry);
        vpos.init();

        /** Wait for the game to begin */
        telemetry.addData(">", "Press Play to start");
        telemetry.update();

        waitForStart();

        while (opModeIsActive()) {

            vpos.updatePosition();

            if (vpos.lastlr != null) {
                telemetry.addData("Position is valid: ", vpos.lrIsValid);

                telemetry.addData("Roll (deg):", vpos.lastlr.rotation.firstAngle);
                telemetry.addData("Pitch:", vpos.lastlr.rotation.secondAngle);
                telemetry.addData("Direction:", vpos.lastlr.rotation.thirdAngle);

                telemetry.addData("X (mm):", (int)vpos.lastlr.location.get(0));
                telemetry.addData("Y:", (int)vpos.lastlr.location.get(1));
                telemetry.addData("Z:", (int)vpos.lastlr.location.get(2));

                telemetry.update();
            }

        }
    }
}